public enum ResultEnum {
    UNKNOWN_ERROR(-1,"未知错误"),
    SUCCESS(0, "成功"),
    EMPTY_FILE(410, "空文件"),
    ERROR_FORMAT_FILE(411, "文件格式错误"),
    IO_EXCEPTION(510, "文件读取时错误");

    private Integer code;

    private String msg;

    ResultEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
